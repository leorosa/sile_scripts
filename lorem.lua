local _, nwords = lorem:gsub("%S+", "")
local ceil = math.ceil
local language = "la"

SILE.registerCommand("lorem_source", function (options, _)
  if options.src == nil then return end
  if options.language then
    language = options.language
  end
  filename = options.src
  lorem = ""
  fileID = io.open(filename, "r")
  for line in io.lines(filename) do
    lorem = lorem .. line
  end
  io.close(fileID)
  _, nwords = lorem:gsub("%S+", "")
end)

local start = 0
SILE.registerCommand("lorem", function (options, _)
  local words = tonumber(options.words) or 50
  local counter = options.counter or false
  local times = ceil(words/nwords)
  local text = lorem:sub(start+1,#lorem)..string.rep(lorem, times)
  local pos = 0
  for _ = 1, words do
    _, pos = text:find("%S+", pos + 1)
  end
  text = text:sub(1, pos)
  start = (start+pos) % #lorem
  if counter then
    local c = 0
    text = string.gsub(text, "(%s+)", function (_)
      c = c + 1
      return " " .. c .. " "
    end)
  end
  SILE.settings.temporarily(function ()
    SILE.settings.set("document.language", language)
    SILE.typesetter:typeset(text)
  end)
end)

return {
  documentation = [[
\begin{document}
Sometimes you just need some dummy text. The command \code{\\lorem}
produces fifty words of “lorem ipsum”; you can choose a different
number of words with the \code{[words=...]} parameter. Here’s
\code{\\lorem[words=20]}:

\examplefont{\lorem[words=20]}
\end{document}
]]
}
